package com.bean;

public class LoveActivity {// 爱心活动类

	public String ac_id;//活动id号
	public String ac_name;//活动名称
	public String ac_time;//活动时间
	public String ac_place;//活动地点
	public String p_name;//活动负责人
	public String p_tel;//活动负责人电话
	public String ac_content;//活动内容
	
	
	public String getAc_id() {
		return ac_id;
	}
	public void setAc_id(String ac_id) {
		this.ac_id = ac_id;
	}
	public String getAc_name() {
		return ac_name;
	}
	public void setAc_name(String ac_name) {
		this.ac_name = ac_name;
	}
	public String getAc_time() {
		return ac_time;
	}
	public void setAc_time(String ac_time) {
		this.ac_time = ac_time;
	}
	public String getAc_place() {
		return ac_place;
	}
	public void setAc_place(String ac_place) {
		this.ac_place = ac_place;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_tel() {
		return p_tel;
	}
	public void setP_tel(String p_tel) {
		this.p_tel = p_tel;
	}
	public String getAc_content() {
		return ac_content;
	}
	public void setAc_content(String ac_content) {
		this.ac_content = ac_content;
	}
	
	

	
	
}
